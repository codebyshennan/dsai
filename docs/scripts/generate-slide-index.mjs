/**
 * Generates _data/slide_index.yml — list of submodule dir keys that have
 * slides/index.html (for “Slides” link in lesson nav). Run from docs/:
 * node scripts/generate-slide-index.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DOCS_ROOT = path.join(__dirname, "..");

const dirs = [];

function walk(dir) {
  const slideIndex = path.join(dir, "slides", "index.html");
  if (fs.existsSync(slideIndex)) {
    const rel = path.relative(DOCS_ROOT, dir).split(path.sep).join("/");
    dirs.push(rel);
  }
  let entries;
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const ent of entries) {
    if (!ent.isDirectory()) continue;
    if (
      ent.name.startsWith(".") ||
      ent.name === "node_modules" ||
      ent.name === "vendor" ||
      ent.name === "_site"
    )
      continue;
    walk(path.join(dir, ent.name));
  }
}

walk(DOCS_ROOT);
dirs.sort();

const lines = [
  "# Auto-generated by scripts/generate-slide-index.mjs — do not edit by hand.",
  "dirs:",
  ...dirs.map((d) => `  - "${d}"`),
];

const out = path.join(DOCS_ROOT, "_data", "slide_index.yml");
fs.writeFileSync(out, lines.join("\n") + "\n", "utf8");
console.log("Wrote", out, `(${dirs.length} dirs)`);
